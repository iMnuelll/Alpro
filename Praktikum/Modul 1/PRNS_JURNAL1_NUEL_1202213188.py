print("===Program Konversi Suhu===")
celcius = int(input("Masukkan Suhu Celcius: "))
celcius_to_reamur = 0.8 * celcius
celcius_to_fahrenheit = (1.8 * celcius) + 32
celcius_to_kelvin = celcius + 273
print(celcius,"Celcius ke Reamur: ",celcius_to_reamur)
print(celcius,"Celcius ke Fahrenheit: ",celcius_to_fahrenheit)
print(celcius,"Celcius ke Kelvin: ",celcius_to_kelvin)